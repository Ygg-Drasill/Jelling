package cmd

import (
	"fmt"
	"os"

	tea "github.com/charmbracelet/bubbletea"
	"github.com/spf13/cobra"
)

type model struct {
	x int
}

func (m model) Init() tea.Cmd {
	return nil
}

func (m model) Update(msg tea.Msg) (tea.Model, tea.Cmd) {
	var cmd tea.Cmd
	switch msg := msg.(type) {
	case tea.KeyMsg:
		switch msg.String() {
		case "q", "ctrl+c":
			cmd = tea.Quit
			break
		case "up":
			m.x++
			break
		case "down":
			m.x--
			break
		}

	}
	return m, cmd
}

func (m model) View() string {
	s := fmt.Sprintf("Hello World %d", m.x)
	return s
}

